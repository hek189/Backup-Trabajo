    private void btEscogerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEscogerActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int returnVal = chooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            entrada.setText(chooser.getSelectedFile().getAbsolutePath());
        }
    }//GEN-LAST:event_btEscogerActionPerformed

