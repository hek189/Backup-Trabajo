    public FilenameFilter comprobarFiltro() {
        FilenameFilter filtro = null;
        String filtroString = (String) comboFiltros.getSelectedItem();
        switch (filtroString) {
            case "Todos los archivos":
                filtro = null;
                break;
            case "Imagenes":
                filtro = filtros.getFiltroImagenes();
                break;
            case "Videos":
                filtro = filtros.getFiltroVideos();
                break;
            case "Documentos":
                filtro = filtros.getFiltroDocumentos();
                break;
            default:
                break;
        }
        return filtro;
    }

